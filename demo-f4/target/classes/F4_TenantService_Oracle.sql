DROP TABLE TENANT_INFO

CREATE TABLE TENANT_INFO
(
	"TENANT_ID" VARCHAR2(50) NOT NULL ENABLE,
	"TENANT_NAME" VARCHAR2(150) NOT NULL ENABLE,
	"DATASOURCE_NAME" VARCHAR2(20) NOT NULL ENABLE, 
	"SCHEMA_NAME" VARCHAR2(50) NOT NULL ENABLE, 
	"TENANT_STATUS" NUMBER(1) NOT NULL,
	"CREATE_AT" DATE, 
	"UPDATE_AT" DATE, 
	 CONSTRAINT "PK_TENANT_INFO" PRIMARY KEY ("TENANT_ID")
)